class 관계연산자
{
	public static void main(String [] args)
	{
		int a= 5, b=3;
		System.out.printf("%d > %d : %b\n",a,b,a>b);
		System.out.printf("%d >= %d : %b\n",a,b,a>=b);
		System.out.printf("%d < %d : %b\n",a,b,a<b);
		System.out.printf("%d <= %d : %b\n",a,b,a<=b);
		System.out.printf("%d == %d : %b\n",a,b,a==b);
		System.out.printf("%d != %d : %b\n",a,b,a!=b);
	}
}